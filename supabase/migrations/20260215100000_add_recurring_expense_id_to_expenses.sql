-- SECTION 4: Add generated_at column for audit
-- Tracks when auto-generation happened
-- ========================================

ALTER TABLE expenses
ADD COLUMN IF NOT EXISTS generated_at TIMESTAMPTZ;

COMMENT ON COLUMN expenses.generated_at IS
'Timestamp when this expense was auto-generated by the recurring system. NULL for manual expenses.';
